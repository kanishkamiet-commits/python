#input two sets and find their intersection
set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}
intersection_set = set1.intersection(set2)
#display the sets and their intersection
print(f"Set 1: {set1}")
print(f"Set 2: {set2}")
#display the intersection of the two sets
print(f"Intersection: {intersection_set}")

'''
Output:
Set 1: {1, 2, 3, 4, 5}
Set 2: {4, 5, 6, 7, 8}
Intersection: {4, 5}
'''